import { formatTimestamp, formatDuration, computeTotalDurationMs } from '../../reports/shared.js';

/**
 * Format verification results as a markdown report.
 */
export function formatVerificationMarkdown(results) {
  const sections = [];

  sections.push(formatHeader(results));
  sections.push(formatSummary(results));
  sections.push(formatUnsyncableWarnings(results));
  sections.push(formatOrgResults(results));
  sections.push(formatProjectResults(results));
  sections.push(formatPortfolios(results));

  sections.push('---\n*Generated by CloudVoyager Verification*\n');

  return sections.filter(Boolean).join('\n');
}

function formatHeader(results) {
  const lines = ['# CloudVoyager Verification Report\n'];
  lines.push(`**Started:** ${formatTimestamp(results.startTime) || results.startTime}  `);
  lines.push(`**Finished:** ${formatTimestamp(results.endTime) || 'In progress'}  `);
  const durationMs = computeTotalDurationMs(results);
  if (durationMs != null) {
    lines.push(`**Duration:** ${formatDuration(durationMs)}  `);
  }
  lines.push('');
  return lines.join('\n');
}

function formatSummary(results) {
  const s = results.summary;
  const lines = ['## Summary\n'];
  const overall = s.failed === 0 && s.errors === 0 ? 'ALL CHECKS PASSED' : `${s.failed} FAILED, ${s.errors} ERRORS`;

  lines.push(`| Metric | Count |`);
  lines.push(`|--------|-------|`);
  lines.push(`| Total checks | ${s.totalChecks} |`);
  lines.push(`| Passed | ${s.passed} |`);
  lines.push(`| Failed | ${s.failed} |`);
  lines.push(`| Warnings (unsyncable) | ${s.warnings} |`);
  lines.push(`| Skipped | ${s.skipped} |`);
  lines.push(`| Errors | ${s.errors} |`);
  lines.push(`| **Overall** | **${overall}** |`);
  lines.push('');
  return lines.join('\n');
}

function formatUnsyncableWarnings(results) {
  if (results.summary.warnings === 0) return '';

  const lines = ['## Unsyncable Items (Expected Differences)\n'];
  lines.push('> These differences are expected and cannot be synced via the SonarCloud API:\n');
  lines.push('- **Issue type changes** — In SonarQube Standard Experience, issue types can be manually changed. This is not API-syncable to SonarCloud. In MQR mode, issue type changes do not exist.');
  lines.push('- **Issue severity changes** — Severity overrides are not API-syncable in either Standard or MQR mode.');
  lines.push('- **Hotspot assignments** — The hotspot sync API does not support assignment transfers.');
  lines.push('');

  // Aggregate unsyncable counts
  let totalTypeChanges = 0, totalSeverityChanges = 0, totalHotspotAssignments = 0;
  for (const project of results.projectResults) {
    if (project.checks.issues?.unsyncable) {
      totalTypeChanges += project.checks.issues.unsyncable.typeChanges || 0;
      totalSeverityChanges += project.checks.issues.unsyncable.severityChanges || 0;
    }
    if (project.checks.hotspots?.unsyncable) {
      totalHotspotAssignments += project.checks.hotspots.unsyncable.assignments || 0;
    }
  }

  if (totalTypeChanges > 0) lines.push(`- Issue type changes: **${totalTypeChanges}** across all projects`);
  if (totalSeverityChanges > 0) lines.push(`- Issue severity changes: **${totalSeverityChanges}** across all projects`);
  if (totalHotspotAssignments > 0) lines.push(`- Hotspot assignment diffs: **${totalHotspotAssignments}** across all projects`);
  lines.push('');
  return lines.join('\n');
}

function formatOrgResults(results) {
  if (results.orgResults.length === 0) return '';

  const lines = ['## Organization-Level Checks\n'];

  for (const org of results.orgResults) {
    lines.push(`### Organization: ${org.orgKey}\n`);

    if (org.error) {
      lines.push(`**Error:** ${org.error}\n`);
      continue;
    }

    const checks = org.checks || {};
    lines.push(`| Check | Status | Details |`);
    lines.push(`|-------|--------|---------|`);

    if (checks.qualityGates) {
      const qg = checks.qualityGates;
      lines.push(`| Quality Gates | ${statusIcon(qg.status)} ${qg.status} | SQ: ${qg.sqCount || 0}, SC: ${qg.scCount || 0}, Missing: ${(qg.missing || []).length}, Condition mismatches: ${(qg.conditionMismatches || []).length} |`);
    }
    if (checks.qualityProfiles) {
      const qp = checks.qualityProfiles;
      lines.push(`| Quality Profiles | ${statusIcon(qp.status)} ${qp.status} | SQ: ${qp.sqCount || 0}, SC: ${qp.scCount || 0}, Missing: ${(qp.missing || []).length}, Rule count mismatches: ${(qp.ruleCountMismatches || []).length} |`);
    }
    if (checks.groups) {
      const g = checks.groups;
      lines.push(`| Groups | ${statusIcon(g.status)} ${g.status} | SQ: ${g.sqCount || 0} custom, SC: ${g.scCount || 0}, Missing: ${(g.missing || []).length} |`);
    }
    if (checks.globalPermissions) {
      const p = checks.globalPermissions;
      lines.push(`| Global Permissions | ${statusIcon(p.status)} ${p.status} | ${(p.mismatches || []).length} groups with missing permissions |`);
    }
    if (checks.permissionTemplates) {
      const t = checks.permissionTemplates;
      lines.push(`| Permission Templates | ${statusIcon(t.status)} ${t.status} | SQ: ${t.sqCount || 0}, SC: ${t.scCount || 0}, Missing: ${(t.missing || []).length} |`);
    }

    lines.push('');

    // Detail missing quality gates
    if (checks.qualityGates?.missing?.length > 0) {
      lines.push(`**Missing quality gates:** ${checks.qualityGates.missing.join(', ')}\n`);
    }
    if (checks.qualityProfiles?.missing?.length > 0) {
      lines.push(`**Missing quality profiles:**`);
      for (const m of checks.qualityProfiles.missing) {
        lines.push(`- ${m.name} (${m.language})`);
      }
      lines.push('');
    }
  }

  return lines.join('\n');
}

function formatProjectResults(results) {
  if (results.projectResults.length === 0) return '';

  const lines = ['## Per-Project Checks\n'];

  for (const project of results.projectResults) {
    const checks = Object.values(project.checks || {});
    const fails = checks.filter(c => c.status === 'fail').length;
    const icon = fails === 0 ? statusIcon('pass') : statusIcon('fail');

    lines.push(`### ${icon} ${project.sqProjectKey} → ${project.scProjectKey}\n`);

    const c = project.checks;

    // Summary table
    lines.push(`| Check | Status | Details |`);
    lines.push(`|-------|--------|---------|`);

    if (c.existence) {
      lines.push(`| Project exists | ${statusIcon(c.existence.status)} ${c.existence.status} | |`);
    }
    if (c.branches) {
      lines.push(`| Branches | ${statusIcon(c.branches.status)} ${c.branches.status} | SQ: ${c.branches.sqCount}, SC: ${c.branches.scCount}, Missing: ${(c.branches.missing || []).length} |`);
    }
    if (c.issues) {
      const iss = c.issues;
      lines.push(`| Issues | ${statusIcon(iss.status)} ${iss.status} | SQ: ${iss.sqCount}, SC: ${iss.scCount}, Matched: ${iss.matched}, Unmatched: ${iss.unmatched} |`);
    }
    if (c.hotspots) {
      const hs = c.hotspots;
      lines.push(`| Hotspots | ${statusIcon(hs.status)} ${hs.status} | SQ: ${hs.sqCount}, SC: ${hs.scCount}, Matched: ${hs.matched}, Unmatched: ${hs.unmatched} |`);
    }
    if (c.measures) {
      lines.push(`| Measures | ${statusIcon(c.measures.status)} ${c.measures.status} | ${c.measures.compared || 0} compared, ${(c.measures.mismatches || []).length} mismatches |`);
    }
    if (c.qualityGate) {
      lines.push(`| Quality Gate | ${statusIcon(c.qualityGate.status)} ${c.qualityGate.status} | SQ: ${c.qualityGate.sqGate || 'none'}, SC: ${c.qualityGate.scGate || 'none'} |`);
    }
    if (c.qualityProfiles) {
      lines.push(`| Quality Profiles | ${statusIcon(c.qualityProfiles.status)} ${c.qualityProfiles.status} | ${(c.qualityProfiles.mismatches || []).length} mismatches |`);
    }
    if (c.settings) {
      lines.push(`| Settings | ${statusIcon(c.settings.status)} ${c.settings.status} | ${(c.settings.mismatches || []).length} mismatches, ${(c.settings.sqOnly || []).length} SQ-only |`);
    }
    if (c.tags) {
      lines.push(`| Tags | ${statusIcon(c.tags.status)} ${c.tags.status} | Missing: ${(c.tags.missing || []).length}, Extra: ${(c.tags.extra || []).length} |`);
    }
    if (c.links) {
      lines.push(`| Links | ${statusIcon(c.links.status)} ${c.links.status} | SQ: ${c.links.sqCount || 0}, SC: ${c.links.scCount || 0}, Missing: ${(c.links.missing || []).length} |`);
    }
    if (c.newCodePeriods) {
      lines.push(`| New Code Periods | ${statusIcon(c.newCodePeriods.status)} ${c.newCodePeriods.status} | SQ: ${c.newCodePeriods.details?.sqProjectLevel || 'default'}, SC: ${c.newCodePeriods.details?.scProjectLevel || 'default'} |`);
    }
    if (c.devopsBinding) {
      lines.push(`| DevOps Binding | ${statusIcon(c.devopsBinding.status)} ${c.devopsBinding.status} | |`);
    }
    if (c.permissions) {
      lines.push(`| Permissions | ${statusIcon(c.permissions.status)} ${c.permissions.status} | ${(c.permissions.mismatches || []).length} groups with missing permissions |`);
    }

    lines.push('');

    // Issue detail breakdowns
    if (c.issues?.statusMismatches?.length > 0) {
      lines.push(`<details><summary>Issue Status Mismatches (${c.issues.statusMismatches.length})</summary>\n`);
      lines.push(`| Rule | File | Line | SQ Status | SC Status |`);
      lines.push(`|------|------|------|-----------|-----------|`);
      for (const m of c.issues.statusMismatches.slice(0, 50)) {
        lines.push(`| ${m.rule} | ${m.file} | ${m.line} | ${m.sqStatus}${m.sqResolution ? '/' + m.sqResolution : ''} | ${m.scStatus}${m.scResolution ? '/' + m.scResolution : ''} |`);
      }
      if (c.issues.statusMismatches.length > 50) lines.push(`\n*... and ${c.issues.statusMismatches.length - 50} more*`);
      lines.push('\n</details>\n');
    }

    if (c.issues?.assignmentMismatches?.length > 0) {
      lines.push(`<details><summary>Issue Assignment Mismatches (${c.issues.assignmentMismatches.length})</summary>\n`);
      lines.push(`| Rule | File | SQ Assignee | SC Assignee |`);
      lines.push(`|------|------|-------------|-------------|`);
      for (const m of c.issues.assignmentMismatches.slice(0, 50)) {
        lines.push(`| ${m.rule} | ${m.file} | ${m.sqAssignee || 'none'} | ${m.scAssignee || 'none'} |`);
      }
      if (c.issues.assignmentMismatches.length > 50) lines.push(`\n*... and ${c.issues.assignmentMismatches.length - 50} more*`);
      lines.push('\n</details>\n');
    }

    if (c.issues?.commentMismatches?.length > 0) {
      lines.push(`<details><summary>Issue Comment Mismatches (${c.issues.commentMismatches.length})</summary>\n`);
      lines.push(`| Rule | File | SQ Comments | SC Migrated Comments |`);
      lines.push(`|------|------|-------------|---------------------|`);
      for (const m of c.issues.commentMismatches.slice(0, 50)) {
        lines.push(`| ${m.rule} | ${m.file} | ${m.sqCommentCount} | ${m.scMigratedCommentCount} |`);
      }
      lines.push('\n</details>\n');
    }

    if (c.issues?.tagMismatches?.length > 0) {
      lines.push(`<details><summary>Issue Tag Mismatches (${c.issues.tagMismatches.length})</summary>\n`);
      lines.push(`| Rule | File | SQ Tags | SC Tags |`);
      lines.push(`|------|------|---------|---------|`);
      for (const m of c.issues.tagMismatches.slice(0, 50)) {
        lines.push(`| ${m.rule} | ${m.file} | ${m.sqTags.join(', ')} | ${m.scTags.join(', ')} |`);
      }
      lines.push('\n</details>\n');
    }

    if (c.hotspots?.statusMismatches?.length > 0) {
      lines.push(`<details><summary>Hotspot Status Mismatches (${c.hotspots.statusMismatches.length})</summary>\n`);
      lines.push(`| Rule | File | Line | SQ Status | SC Status |`);
      lines.push(`|------|------|------|-----------|-----------|`);
      for (const m of c.hotspots.statusMismatches.slice(0, 50)) {
        lines.push(`| ${m.rule} | ${m.file} | ${m.line} | ${m.sqStatus}${m.sqResolution ? '/' + m.sqResolution : ''} | ${m.scStatus}${m.scResolution ? '/' + m.scResolution : ''} |`);
      }
      if (c.hotspots.statusMismatches.length > 50) lines.push(`\n*... and ${c.hotspots.statusMismatches.length - 50} more*`);
      lines.push('\n</details>\n');
    }
  }

  return lines.join('\n');
}

function formatPortfolios(results) {
  if (!results.portfolios || results.portfolios.sqCount === 0) return '';

  const lines = ['## Portfolios\n'];
  lines.push(`Status: ${results.portfolios.status} — ${results.portfolios.details}\n`);
  if (results.portfolios.sqPortfolios?.length > 0) {
    lines.push(`SonarQube portfolios (${results.portfolios.sqCount}):`);
    for (const p of results.portfolios.sqPortfolios) {
      lines.push(`- ${p.name} (\`${p.key}\`)`);
    }
    lines.push('');
  }
  return lines.join('\n');
}

function statusIcon(status) {
  if (status === 'pass') return '✅';
  if (status === 'fail') return '❌';
  if (status === 'skipped') return '⏭️';
  if (status === 'error') return '⚠️';
  return '❓';
}
